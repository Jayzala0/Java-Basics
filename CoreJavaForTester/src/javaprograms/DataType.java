package javaprograms;

public class DataType {

	public static void main(String[] args) {

		
		System.out.println("-----------Frequently used data types-----------");
		
		int val = 100;   //int -> DataType   val -> Variable   100 -> value
		double d = 1000.20;
		boolean b = true; // or false
		String str = "Welcome To Unicode Technologies Pvt Ltd";
	
		
		//System.out.println("100");
		System.out.println(val);   //100 will print
		System.out.println("val");  //val will print
		System.out.println(d);
		System.out.println(b);
		System.out.println(str);
		
	}

}
